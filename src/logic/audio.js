/**
 * Finds the audio file for the given chapter in the fragment.
 * @param {int[]} fragment [bookIndex, chapterIndex, startVerse, endVerse]
 */
export function audioSource(fragment) {
  if (!fragment) return ''
  const chapter = fragment[1]
  const index = (books.chapterCount.slice(0, fragment[0]).reduce((acc, cur) => acc + cur, 0) + chapter)
  return ccnz[index]
}

